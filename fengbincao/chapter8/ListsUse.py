# 本文件演示的是Python的基本语法，列表相关的使用

# 1.判断列表中是否存在某个元素
print(3 in [1, 2, 3])

# 2.获取列表的长度
print("长度为" + str(len([1, 2, 3])))

# 3.列表相加
print([1, 2, 3] + [4, 5, 6])

# 4.reprtion列表
print([1, 2, 3] * 2)

# 5.将列表转化为字符串的函数，输出结果为：[1, 2]34
print(str([1, 2]) + "34")

# 6.将字符串转化为列表形式
print([1, 2] + list("34"))

# 7.列表迭代操作
for x in [1, 2, 3, 34, 55]:
    print(x, end='  ')
